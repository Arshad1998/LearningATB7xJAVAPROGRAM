package ex_14072024;

public class Lab079 {
    public static void main(String[] args) {
        //  Ternary operator.
        //  ? if true do this : ele do that
        // condition ? true : false

        int result = 100>40 ? 89 : 91;
        System.out.println(result);

        int a1 = (30>40) ? 10: 20;
        System.out.println(a1);

        String str= 10>20 ? "10" : "TWENTY";
        System.out.println(str);
    }
}
