package ex_13072024;

public class Lab023 {
    public static void main(String[] args) {

        int age = 98;
        char grade = 'A';
        String name = "Arshad";
        System.out.println("Your age is -> " + age);

        // Formatting
        // %d ->  any integer - byte, short, int or long
        // %s -> String
        // %c -> char
        // %f -> float
        System.out.printf("Your age is -> %d" ,age);
        System.out.println();
        System.out.printf("My grade is -> %c" ,grade);
        System.out.println();
        System.out.printf("My name is is -> %s", name);



    }
}
